
public class ChildInfo {
	private int child;
	private double fitnessPoint;
	
	public ChildInfo(int child, double fitnessPoint) {
		this.child = child;
		this.fitnessPoint = fitnessPoint;
	}

	public int getChild() {
		return child;
	}

	public double getFitnessPoint() {
		return fitnessPoint;
	}
	
}
